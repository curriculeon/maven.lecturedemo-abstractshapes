public class Square extends Rectangle implements Shape {
    public Square(Integer width) {
        super(width);
    }

    public Square() {
    }

    @Override
    public Integer getHeight() {
        return getHeight();
    }

    @Override
    public Integer getWidth() {
        return getWidth();
    }

    @Override
    public String getDimensions() {
        return getDimensions();
    }
}
